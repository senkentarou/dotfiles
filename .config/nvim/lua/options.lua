--
-- Vim options
--
vim.cmd([[
  set guifont=Droid\ Sans\ Mono\ for\ Powerline\ Nerd\ Font\ Complete\ 12
  set undofile
  set cmdheight=2
  set ignorecase
  set hidden
  set autoread
  set noshowmode
  " Colorscheme
  set termguicolors
  colorscheme hybrid
  " File encoding
  set encoding=utf-8
  " Line settings
  set number
  set backspace=indent,eol,start
  set whichwrap=b,s,<,>,[,],h,l
  set cursorline
  " Clipboard sharing
  set clipboard=unnamed
  " Completion settings
  set completeopt=menuone,noinsert,noselect
  set shortmess+=c
  " Tab settings
  set expandtab
  set smarttab
  set shiftwidth=2
  set softtabstop=2
  set tabstop=2
]])
